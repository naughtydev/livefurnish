var site = {
	init: function() {
		site.accordion();
		site.mainSlider();
		site.thumbnailSlider();
	},

	mainSlider: function() {
		jQuery('.main-slider').slick({
			infinite: false,
			asNavFor: '.thumbnail-slider',
			dots: false,
			arrows: false,
			slidesToShow: 1,
			slidesToScroll: 1
		});

		jQuery('.main-slider .slider-item').zoom();
	},

	thumbnailSlider: function() {
		jQuery('.thumbnail-slider').slick({
			infinite: false,
			asNavFor: '.main-slider',
			variableWidth: false,
			centerMode: true,
			centerPadding: '0',
			speed: 0,
			draggable: false,
			dots: false,
			arrows: false,
			slidesToShow: 5,
			slidesToScroll: 1
		});
	},

	accordion: function() {
		jQuery('.accordion-heading').click(function() {
			var contentEl = jQuery(this).parents('.accordion-wrapper');

			if (contentEl.hasClass('active')) {
				contentEl.removeClass('active');
				contentEl.find('.accordion-content').slideUp();
			} else {
				jQuery('.accordion-wrapper').removeClass('active');
				jQuery('.accordion-content').slideUp();

				contentEl.addClass('active');
				contentEl.find('.accordion-content').slideDown();
			}
		});

		jQuery('.accordion-wrapper .accordion-selection').click(function() {

			var _this = jQuery(this);

			if (_this.hasClass('active')) {
				return false;
			}

			jQuery('body').append('<span class="loader" style="display: block;"></span>');

			var targetEl = jQuery(this).data('target');
			var imagesData = images[targetEl].split(',');
			var sliderData = '';

			if (imagesData.length > 0) {

				for (var i = 0; i < imagesData.length; i++) {
					sliderData += '<div class="slider-item"><img src="'+imagesData[i]+'" /></div>';
				}

				var csimage = new Image();

				csimage.src = imagesData[0];

				csimage.onload = function() {
					jQuery('.main-slider').removeClass('slick-initialized slick-slider').html(sliderData);
					jQuery('.thumbnail-slider').removeClass('slick-initialized slick-slider').html(sliderData);
					site.mainSlider();
					site.thumbnailSlider();

					jQuery('.accordion-wrapper .accordion-selection').removeClass('active');
					_this.addClass('active');
					jQuery('body .loader').remove();
				}
			}

			return false;
		});
	}
}

jQuery(document).ready(function() {
	site.init();
});